

public class Dato 
{
	private int dag;
	private int måned;
	private int år;
	
	public Dato (int d, int m, int å)
	{
		dag = d;
		måned = m;
		år = å;
	}
	
	public int getDag()
	{
		return dag;
	}
	
	public int getMåned()
	{
		return måned;
	}
	
	public int getÅr()
	{
		return år;
	}
	
	
}
